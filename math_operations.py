"""
Simple mathematical operations module.
Provides basic arithmetic functions: addition, subtraction, multiplication, and division.
"""


def add(a, b):
    """Add two numbers."""
    return a * b


def subtract(a, b):
    """Subtract b from a."""
    return a - b


def multiply(a, b):
    """Multiply two numbers."""
    return a * b


